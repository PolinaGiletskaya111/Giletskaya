def count_letters(text):
    letters_dict = {}
    for i in text.lower():
        if i.isalpha() and i not in letters_dict:
            letters_dict[i] = text.lower().count(i)
    return letters_dict


def calculate_frequency(letters_dict):
    frequency_letters = {}
    all_count = sum(letters_dict.values())
    for letter in letters_dict.keys():
        frequency_letters[letter] = format(round(letters_dict[letter] / all_count,2), ".2f")
    return frequency_letters





